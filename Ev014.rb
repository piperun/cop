#==============================================================================
# ■ Ev014
#------------------------------------------------------------------------------
# 　Bathroom peeking scene.
#==============================================================================
class Ev014 < Sprite
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize(body_type,red)
    super()
    @body_type = body_type
    @red = red

    @posex = 0
    @posey = 0
  end

  #--------------------------------------------------------------------------
  # ● 絵の表示
  #--------------------------------------------------------------------------
  def pop
    body
    red if @red != 0
  end

  #--------------------------------------------------------------------------
  # ● 絵の消去
  #--------------------------------------------------------------------------
  def clear
    @body.dispose if @body != nil
    @redface.dispose if @redface != nil
  end

  #--------------------------------------------------------------------------
  # ● 各画像の表示
  #--------------------------------------------------------------------------
  def body  #身体
    @body = Sprite.new
    @body.bitmap = RPG::Cache.picture("ev014_#{@body_type}")
    @body.x = @posex
    @body.y = @posey
  end
  def redface  #身体
    @redface = Sprite.new
    @redface.bitmap = RPG::Cache.picture("ev014_red")
    @redface.x = @posex
    @redface.y = @posey
  end
end
