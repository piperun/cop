#==============================================================================
# ■ Ev009
#------------------------------------------------------------------------------
# 　Under table mini-scene.
#==============================================================================
class Ev009 < Sprite
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize(body_type,liquid)
    super()
    @body_type = body_type
    @liquid = liquid

    @posex = 0
    @posey = 0
  end

  #--------------------------------------------------------------------------
  # ● 絵の表示
  #--------------------------------------------------------------------------
  def pop
    body
    liquid_type
  end

  #--------------------------------------------------------------------------
  # ● 絵の消去
  #--------------------------------------------------------------------------
  def clear
    @body.dispose if @body != nil
    @liquid_type.dispose if @liquid_type != nil
  end

  #--------------------------------------------------------------------------
  # ● 各画像の表示
  #--------------------------------------------------------------------------
  def body
    @body = Sprite.new
    @body.bitmap = RPG::Cache.picture("ev009_body#{@body_type}")
    @body.x = @posex
    @body.y = @posey
  end
  def liquid_type
    @head = Sprite.new
    @head.bitmap = RPG::Cache.picture("ev009_liquid#{@liquid}")
    @head.x = @posex
    @head.y = @posey
  end
end
